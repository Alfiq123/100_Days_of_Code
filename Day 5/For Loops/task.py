# The Loops
fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit)

print("")

tools = ["Screwdriver", "Wrench", "Pliers", "Mallet", "Sledgehammer"]
for tool in tools:
    print(tool)

print("")

fruitout = ["Apple", "Peach", "Pear", "Pumpkin"]
for fruitin in fruitout:
    print(fruitin)
    print(fruitin, "pie")
